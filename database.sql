-- Reset existing tables (this wipes the previous dummy data so we can recreate securely)
DROP TABLE IF EXISTS public.reservations CASCADE;
DROP TABLE IF EXISTS public.articles CASCADE;
DROP TABLE IF EXISTS public.doctors CASCADE;

-- 1. Create Storage Bucket for Images (Doctors & Articles)
INSERT INTO storage.buckets (id, name, public) VALUES ('clinic-images', 'clinic-images', true) ON CONFLICT DO NOTHING;

-- Storage Policies
-- Allow public access to read images
CREATE POLICY "Public Read Access" ON storage.objects FOR SELECT USING (bucket_id = 'clinic-images');
-- Allow authenticated admins to upload/update/delete images
CREATE POLICY "Admin Upload Access" ON storage.objects FOR INSERT WITH CHECK (bucket_id = 'clinic-images' AND auth.role() = 'authenticated');
CREATE POLICY "Admin Update Access" ON storage.objects FOR UPDATE USING (bucket_id = 'clinic-images' AND auth.role() = 'authenticated');
CREATE POLICY "Admin Delete Access" ON storage.objects FOR DELETE USING (bucket_id = 'clinic-images' AND auth.role() = 'authenticated');


-- 2. Create Doctors Table
CREATE TABLE public.doctors (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  specialty TEXT NOT NULL,
  image_url TEXT,
  schedule TEXT,
  experience TEXT,
  rating NUMERIC(3, 1),
  bio TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- Doctor RLS Policies
ALTER TABLE public.doctors ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public profiles are viewable by everyone." ON public.doctors FOR SELECT USING (true);
CREATE POLICY "Admins can insert/update doctors" ON public.doctors FOR ALL USING (auth.role() = 'authenticated');

