-- 4. Create Reservations Table
CREATE TABLE public.reservations (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  reservation_code TEXT UNIQUE NOT NULL,
  patient_name TEXT NOT NULL,
  patient_phone TEXT NOT NULL,
  doctor_id BIGINT REFERENCES public.doctors(id) ON DELETE SET NULL,
  reservation_date DATE NOT NULL,
  reservation_time TIME NOT NULL,
  complaint TEXT,
  status TEXT DEFAULT 'pending' CHECK (status IN ('pending', 'confirmed', 'cancelled')),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- Reservation RLS Policies
ALTER TABLE public.reservations ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Anyone can create a reservation" ON public.reservations FOR INSERT WITH CHECK (true);
CREATE POLICY "Only admins can view/update reservations" ON public.reservations FOR SELECT USING (auth.role() = 'authenticated');
CREATE POLICY "Only admins can update reservations" ON public.reservations FOR UPDATE USING (auth.role() = 'authenticated');

